// Define the while loop condition
let count = 1; // Initialize a variable to control the loop

while (count <= 5) { 
  console.log(`Count is: ${count}`); // Code to be executed in each iteration
  count++; // Update the loop control variable
}

console.log("Loop finished!"); // Code executed after the loop

// Define the for loop loop condition
for (let count = 1; count <= 5; count++) {
      console.log(`Count is: ${count}`);
}
    